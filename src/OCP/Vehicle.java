package OCP;

public class Vehicle {
    private int maxSpeed;
    private String type;

    // Метод для расчета допустимой скорости (по умолчанию возвращает максимальную скорость)
    public double calculateAllowedSpeed() {
        return maxSpeed;
    }

    public int getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(int maxSpeed) {
        this.maxSpeed = maxSpeed;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    // Конструктор класса Vehicle, принимающий максимальную скорость и тип транспортного средства
    public Vehicle(int maxSpeed, String type) {
        this.maxSpeed = maxSpeed;
        this.type = type;
    }
}
//Свойства maxSpeed и type: Эти приватные поля класса Vehicle хранят максимальную скорость и тип транспортного средства соответственно. Они инкапсулируют данные, что способствует безопасности и контролю над ними.

//Метод calculateAllowedSpeed(): Этот метод возвращает максимальную скорость, и он является виртуальным (не переопределенным) в базовом классе. Он может быть переопределен в производных классах, чтобы предоставить специфичную логику расчета допустимой скорости для каждого типа транспортного средства. В базовом классе он просто возвращает максимальную скорость, что является значением по умолчанию.

//Геттеры и сеттеры для maxSpeed и type: Геттеры и сеттеры обеспечивают доступ к приватным полям maxSpeed и type. Они позволяют установить и получить значения этих полей, соблюдая инкапсуляцию данных.

//Конструктор класса Vehicle: Конструктор принимает максимальную скорость и тип транспортного средства, инициализирует соответствующие поля объекта. Это обеспечивает удобное создание объектов Vehicle с указанием необходимых данных.
